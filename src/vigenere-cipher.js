class VigenereCipheringMachine {
    constructor(direct = true) {
        this.direct = direct
    }

    encrypt(message, key) {
        if (!(message && key)) {
            throw new Error;
        }
        return cryptStr(message, key, this.direct);
    }

    decrypt(encryptedMessage, key) {
        if (!(encryptedMessage && key)) {
            throw new Error;
        }
        return cryptStr(encryptedMessage, key, this.direct, false);
    }
}

