function fillKeyStr(message, key) {
    let keyStr = '';    
    let i = 0;
    let keyLetterIndex = 0;
    while (i < message.length) {
        if (squareLegend.hasOwnProperty(message[i])) {
            keyStr += key[keyLetterIndex];
            keyLetterIndex == key.length - 1 ? keyLetterIndex = 0 : keyLetterIndex++;             
        }
        i++;
    } 
    return keyStr;
}

function cryptStr(str, key, direct, isEncrypt = true) {
    str = str.toLowerCase();
    key = fillKeyStr(str, key).toLowerCase();
    let cryptedStr = '';
    for (let i = 0, j = 0; i < str.length; i++) {
        if (squareLegend.hasOwnProperty(str[i])) {
            cryptedStr += isEncrypt ? squareVigenere[squareLegend[key[j]]][squareLegend[str[i]]]
                                    : squareVigenere[0][squareVigenere[squareLegend[key[j]]].indexOf(str[i])];
            j++;            
        } else {
            cryptedStr += str[i];            
        }
    }    
    if (!direct) {
        cryptedStr = cryptedStr.split('').reverse().join('');
    }        
    return cryptedStr.toUpperCase();
}